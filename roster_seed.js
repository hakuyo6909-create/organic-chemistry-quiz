/* ============================================================
   有機化学クイズ — 既定の生徒名簿（初回起動時に自動登録）
   ------------------------------------------------------------
   共有された出席番号一覧をアプリ標準搭載の名簿として定義する。
   ログインは4桁の出席番号のみ（イニシャル不要）。
   auth_ui.js の初回起動時に roster ストアへ自動投入される
   （meta フラグ rosterSeededV1 で二重投入・削除後の復活を防止）。

   追加・削除は下の STUDENT_IDS を編集するだけでよい。
   ============================================================ */
(function (global) {
  'use strict';

  // 出席番号（学年1桁 + 組1桁 + 番号2桁）
  const STUDENT_IDS = [
    // 3年1組
    '3101', '3102', '3103', '3105', '3107', '3108', '3109', '3110',
    '3111', '3114', '3115', '3116', '3117', '3118', '3119', '3120',
    '3125', '3126', '3127', '3128', '3130', '3132', '3133', '3134',
    '3135', '3137', '3140', '3141',
    // 3年2組
    '3201', '3203', '3205', '3206', '3209', '3210', '3211', '3216',
    '3217', '3218', '3219', '3220', '3222', '3225', '3230', '3231',
    '3232', '3233', '3234', '3235', '3237', '3238', '3240',
  ];

  // roster ストアのレコード形式に整形（name/initial は空でよい）
  global.OrgQuizDefaultRoster = STUDENT_IDS.map(function (id) {
    return {
      id: id,
      role: 'student',
      grade: Number(id[0]),
      class: Number(id[1]),
      number: Number(id.slice(2, 4)),
      name: '',
      initial: '',
    };
  });

  // 名簿の版。中身を変えたら数字を上げると全端末で再投入される。
  global.OrgQuizDefaultRosterVersion = 1;
})(window);
